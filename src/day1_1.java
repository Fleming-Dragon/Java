public class day1_1 {
    public static void main(String ved[])
    {
        //program to create swapping


        int a = 100;
        int b = 200;

        System.out.println("value of a : "+ a );
        System.out.println("value of b : "+ b );

        int temp = a ;
        a = b ;
        b = temp;

        System.out.println("value of a : "+ a );
        System.out.println("value of b : "+ b );


    }
}
